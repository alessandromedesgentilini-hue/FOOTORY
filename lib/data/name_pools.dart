String nomeAleatorio(String pais, Random r) {
  final fn = (kFirstNames[pais] ?? kFirstNames['BR']!)..shuffle(r);
  final ln = (kLastNames[pais] ?? kLastNames['BR']!)..shuffle(r);
  // 3 formas: "Nome Sobrenome", "Nome", "Nome, Sobrenome"
  final modo = r.nextInt(3);
  if (modo == 0) return '${fn.first} ${ln.first}';
  if (modo == 1) return fn.first;
  return '${fn.first} ${ln.first}';
}
